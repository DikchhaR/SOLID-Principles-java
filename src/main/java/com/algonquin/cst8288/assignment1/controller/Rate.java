package com.algonquin.cst8288.assignment1.controller;
/**
 * This is a class Rate that defines constants related to employee compensation calculations.
 * @author rijal
 */
public class Rate {
    /**
     * The percentage of salary used to calculate the employee's bonus.
     * This value is 2.5% (0.025) of the employee's salary.
     */
    public static final double BONUS_PERCENTAGE = 0.025;
    
    /**
     * The percentage of salary used to calculate the employee's pension contribution.
     * This value is 1% (0.01) of the employee's salary.
     */    
    public static final double PENSION_PERCENTAGE = 0.01;
}

